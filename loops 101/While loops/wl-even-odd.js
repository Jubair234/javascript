/***

Subtask-1:

Find all the odd numbers from 61 to 100.

 */

let numbers = 61;
while(numbers <= 100){
    if(numbers % 2 === 1){
        console.log('Odd number:', numbers);
    }
    
    numbers++;
}

/***

Subtask-2:

Find all the even numbers from 78 to 98.

 */
let numbers1 = 78;
while(numbers1 <= 98){
    if(numbers1 % 2 === 0){
        console.log('Even number:', numbers1);
    }
    
    numbers1++;
}





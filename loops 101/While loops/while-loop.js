// Even number 

let num  = 1;
while(num <= 10){
    console.log(num);
    if(num % 2 === 0){
        console.log('Even number', num)

    }
    num++;
}

// OOD number 

let num1  = 1;
while(num1 <= 10){
    console.log(num1);
    if(num1 % 2 === 1){
        console.log('OOD number', num1)

    }
    num1++;
}

